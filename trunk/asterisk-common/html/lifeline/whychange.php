	<h1>Lifeline Voice mail: why change?</h1>
	The original DERA voice mail system started operations in 1998 and we've 
	been using the same system since then. At some point the system will stop
	working and we wanted to avoid any downtime for you by building a new one
	before that happens.
	<p>
	One downside to all of this is that we have had to switch to a new phone service 
	provider. We have not changed phone providers because we 
	are unhappy with our old provider's service. Rather, our needs have evolved and they do 
	not offer the type of service we need.
	<p>
	The telephone industry has changed drastically since 1998 which means that
	it is now feasible to keep our original phone number when moving to a new 
	phone service provider. Similarly, we have a new, more flexible platform
	for the service that allows us to make the system more robust.
	<p>
	One big plus to our new platform is that it is now very easy for us to 
	provide this service in other areas of Canada. If you think low cost 
	voice mail would be a good fit for your community please contact us.
